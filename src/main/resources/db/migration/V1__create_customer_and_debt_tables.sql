CREATE TABLE TB_CUSTOMER (
    COD_ID                  INTEGER GENERATED BY DEFAULT AS IDENTITY,
	NAM_NAME                VARCHAR(50) NOT NULL,
	COD_DOCUMENT            VARCHAR(15) UNIQUE NOT NULL,
	DES_PRIORITY_WAY        VARCHAR(15),
	NAM_MAIL                VARCHAR(50),
	COD_PHONE               VARCHAR(15),
	DES_ADDRESS             VARCHAR(255),
	NUM_PROFILE_SCORE       INTEGER NOT NULL DEFAULT 500,
	DAT_CREATION            TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
	PRIMARY KEY(COD_ID)
);

CREATE TABLE TB_CUSTOMER_DEBT(
    COD_ID                  INTEGER GENERATED BY DEFAULT AS IDENTITY,
    COD_CUSTOMER_ID         INTEGER NOT NULL,
    NUM_AMOUNT              NUMERIC(10,2) NOT NULL,
    NUM_MONTHLY_TAX         NUMERIC(4,2),
    DAT_DUE_DATE            DATE,
    NUM_OVERDUE_DAYS        INTEGER,
    DAT_LAST_CHARGE_DATE    INTEGER,
    COD_CREDITOR_DOCUMENT   VARCHAR(15),
    COD_NEGOTIATION_TICKET_ID   INTEGER,
    DAT_CREATION            TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY(COD_ID),
    FOREIGN KEY (COD_CUSTOMER_ID) REFERENCES TB_CUSTOMER(COD_ID)
);